CREATE TABLE cozinha(
    id BIGINT(19) PRIMARY KEY AUTO_INCREMENT,
    nome VARCHAR(120) NOT NULL
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE restaurante(
    id BIGINT(19) PRIMARY KEY AUTO_INCREMENT,
    nome VARCHAR(120) NOT NULL,
    taxa_frete DECIMAL(19,5) DEFAULT 0
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

